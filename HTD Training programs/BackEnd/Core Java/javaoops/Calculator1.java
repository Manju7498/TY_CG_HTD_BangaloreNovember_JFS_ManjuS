package javaOops;

public class Calculator1 {
	int add(int a) {
		return a+a;
	}
	int add(int a,int b) {
		return a+b;
	}
	double add(double a) {
		return a+a;
	}
	static int mul(int a) {
		return a*a;
	}
	static int mul(int a,int b) {
		return a*b;
	}
	static double mul(double a) {
		return a*a;
	}

}
